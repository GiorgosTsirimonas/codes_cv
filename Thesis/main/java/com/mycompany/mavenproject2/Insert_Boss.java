package com.mycompany.mavenproject2;

import java.io.IOException;
import java.io.PrintWriter;
import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;
import java.util.logging.Level;
import java.util.logging.Logger;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;


/**
 *
 * @author giorgos tsirimonas
 */
@WebServlet(name = "Insert_Boss", urlPatterns = {"/Insert_Boss"})
public class Insert_Boss extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            
            String name = request.getParameter("name"),  password = request.getParameter("password"), address = request.getParameter("address");
         
            int Overtimes = Integer.parseInt(request.getParameter("Overtimes")), Daysoff = Integer.parseInt(request.getParameter("Daysoff"));
            int working_years = Integer.parseInt(request.getParameter("workingyears")),  age = Integer.parseInt(request.getParameter("Age"));
            int salary = Integer.parseInt(request.getParameter("salary")), children = Integer.parseInt(request.getParameter("children"));
        
            try {
                Class.forName("org.apache.derby.jdbc.ClientDriver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Insert_Boss.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/BigCorporation", "app", "app");
            Statement stmt = conn.createStatement();
            stmt.executeUpdate("INSERT INTO BOSS (NAME,PASSWORD,CHILDREN,ADDRESS,AGE,SALARY,WORKING_YEARS,OVERTIMES,DAYSOFF)" + "VALUES ('"+name+"','"+password+"',"+children+",'"+address+"',"+age+","+salary+","+working_years+","+Overtimes+","+Daysoff+")");
            response.sendRedirect("Boss_Home");
            conn.close();
            stmt.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(Insert_Boss.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
